param(
    [switch]$Check
)

$ErrorActionPreference = "Stop"
$RepoRoot = Resolve-Path (Join-Path $PSScriptRoot "..")
$WidgetRoot = Join-Path $RepoRoot "widgets"

$manifests = Get-ChildItem -Path $WidgetRoot -Filter "widget.json" -File -Recurse |
    Sort-Object FullName |
    ForEach-Object {
        $manifest = Get-Content -LiteralPath $_.FullName -Raw | ConvertFrom-Json
        if ($manifest.schemaVersion -ne 1) { throw "Unsupported widget manifest version: $($_.FullName)" }
        if ($manifest.id -notmatch '^[a-z][a-z0-9-]{2,63}$') { throw "Invalid widget id: $($manifest.id)" }
        if ([string]::IsNullOrWhiteSpace($manifest.displayName)) { throw "Widget displayName is required: $($_.FullName)" }
        if ($manifest.defaultSize.width -lt 32 -or $manifest.defaultSize.width -gt 480) { throw "Invalid width: $($manifest.id)" }
        if ($manifest.defaultSize.height -lt 24 -or $manifest.defaultSize.height -gt 80) { throw "Invalid height: $($manifest.id)" }
        $manifest
    }

$duplicate = $manifests | Group-Object id | Where-Object Count -gt 1 | Select-Object -First 1
if ($duplicate) { throw "Duplicate widget id: $($duplicate.Name)" }
if ($manifests.Count -eq 0) { throw "No widget manifests were found." }

$csItems = ($manifests | ForEach-Object { "        `"$($_.id)`"" }) -join ",`r`n"
$csharp = @"
// <auto-generated />
#nullable enable
namespace TaskbarWidgets.Loader.Core;

internal static class WidgetCatalog
{
    public static readonly string[] KnownIds =
    [
$csItems
    ];

    public static bool IsKnown(string? id) =>
        id is not null && KnownIds.Contains(id, StringComparer.OrdinalIgnoreCase);
}
"@

$cppItems = ($manifests | ForEach-Object {
    "    {L`"$($_.id)`", L`"$($_.displayName)`", $($_.defaultSize.width).0, $($_.defaultSize.height).0}"
}) -join ",`r`n"
$cpp = @"
// <auto-generated />
#pragma once
#include <array>
#include <string_view>

namespace taskbar_widgets::generated {
struct WidgetDefinition { std::wstring_view id; std::wstring_view displayName; double width; double height; };
inline constexpr std::array<WidgetDefinition, $($manifests.Count)> kWidgets{{
$cppItems
}};
}  // namespace taskbar_widgets::generated
"@

$jsJson = $manifests | ConvertTo-Json -Depth 10 -Compress
$javascript = "// <auto-generated />`r`nwindow.TASKBAR_WIDGET_CATALOG = $jsJson;`r`n"

$outputs = @{
    (Join-Path $RepoRoot "src\loader\Generated\WidgetCatalog.g.cs") = $csharp
    (Join-Path $RepoRoot "src\native\taskbar-hook\generated\widget_catalog.g.h") = $cpp
    (Join-Path $RepoRoot "src\settings\dist\generated\widget-catalog.js") = $javascript
}

$utf8 = New-Object System.Text.UTF8Encoding($false)
$changed = $false
foreach ($entry in $outputs.GetEnumerator()) {
    $path = $entry.Key
    $content = ($entry.Value.TrimEnd() + "`r`n")
    $current = if (Test-Path -LiteralPath $path) { [IO.File]::ReadAllText($path) } else { "" }
    if ($current -ne $content) {
        $changed = $true
        if (-not $Check) {
            New-Item -ItemType Directory -Force (Split-Path $path -Parent) | Out-Null
            [IO.File]::WriteAllText($path, $content, $utf8)
        }
    }
}

if ($Check -and $changed) { throw "Generated widget catalogs are out of date. Run build\generate-widget-catalog.ps1." }
Write-Host "Validated $($manifests.Count) widget manifests."
